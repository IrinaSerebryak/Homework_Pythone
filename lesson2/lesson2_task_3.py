def square(side):
    """
    Функция для вычисления площади квадрата.
    Args:
        side: Длина стороны квадрата (число)
    Returns:
        Площадь квадрата
    """
    return side * side
if __name__ == "__main__":
        print(square(8))
        print(square(13))
        print(square(15))


